fun main() {
    println(a())
    println(b())
}

private fun a(stepX: Int = 3, stepY: Int = 1): Int {
    val positions = generateSequence(Pair(0, 0)) { it.next(stepX, stepY) }.takeWhile { it.second < forest.size }
    return positions.count {
        val x = it.first % forest[0].length // All rows are same length, so we just modulo any arbitrary row.
        val y = it.second
        forest[y][x] == '#'
    }
}

private fun b(): Long {
    val slopes = listOf(Pair(1, 1), Pair(3, 1), Pair(5, 1), Pair(7, 1), Pair(1, 2))
    return slopes.map { a(it.first, it.second) }.product()
}

private fun Pair<Int, Int>.next(stepX: Int, stepY: Int) = this.copy(this.first + stepX, this.second + stepY)

